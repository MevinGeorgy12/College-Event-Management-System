    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                    
    // 1. Get all the data from the form fields
    String studentName = jTextField1.getText();
    String collegeName = jTextField2.getText();
    String studyYear = (String) jComboBox1.getSelectedItem();
    String branch = (String) jComboBox2.getSelectedItem();
    String workshopName = (String) jComboBox3.getSelectedItem(); // Renamed for clarity

    // 2. Validate the input
    if (studentName.isEmpty() || collegeName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Student Name and College Name cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Stop if validation fails
    }

    // Check if a valid workshop was selected from the dropdown
    if (workshopName == null || workshopName.equals("-- Select Workshop --")) { // Adjust if you didn't use the default text
        JOptionPane.showMessageDialog(this, "Please select a valid workshop.", "Warning", JOptionPane.WARNING_MESSAGE);
        return; // Stop if no valid workshop is selected
    }

    // 3. Prepare the SQL INSERT statement for the 'registrations' table
    String sql = "INSERT INTO registrations (student_name, study_year, branch, college_name, event_name) VALUES (?, ?, ?, ?, ?)";

    // 4. Use a try-with-resources block for database operation
    try (Connection con = DBConnection.getConnection();
         PreparedStatement pst = con.prepareStatement(sql)) {

        // Set the values for the '?' placeholders
        pst.setString(1, studentName);
        pst.setString(2, studyYear);
        pst.setString(3, branch);
        pst.setString(4, collegeName);
        pst.setString(5, workshopName); // Use the selected workshop name

        // Execute the query
        int affectedRows = pst.executeUpdate();

        // 5. Provide feedback to the user
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Workshop Registration Successful!");
            
            // Clear the form fields after success
            jTextField1.setText("");
            jTextField2.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);
            jComboBox3.setSelectedIndex(0); // Reset workshop dropdown
        } else {
            JOptionPane.showMessageDialog(this, "Registration Failed. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton1ActionPerformed

