    private void BrowseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseButtonActionPerformed
// 1. Create a new JFileChooser object
    JFileChooser fileChooser = new JFileChooser();

    // 2. Create a filter to only show image files (optional but recommended)
    FileNameExtensionFilter imageFilter = new FileNameExtensionFilter(
        "Image Files", "jpg", "png", "gif", "jpeg");
    fileChooser.setFileFilter(imageFilter);

    // 3. Show the "Open" dialog box and wait for the user's response
    int result = fileChooser.showOpenDialog(this);

    // 4. Check if the user selected a file (and didn't click "Cancel")
    if (result == JFileChooser.APPROVE_OPTION) {
        // 5. Get the selected file
        java.io.File selectedFile = fileChooser.getSelectedFile();
        
        // 6. Get the absolute path of the file and set it in the text field
        String filePath = selectedFile.getAbsolutePath();
        imagePathField.setText(filePath);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_BrowseButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
String workshopName = workshopNameField.getText(); // Assumes your workshop name field is named 'workshopNameField'
    String imagePath = imagePathField.getText();

    // 2. Validate that the fields are not empty
    if (workshopName.isEmpty() || imagePath.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Workshop Name and Image Path cannot be empty.", "Warning", JOptionPane.WARNING_MESSAGE);
        return; // Stop the method
    }

    // 3. Prepare the SQL query to insert data into the 'workshops' table
    String sql = "INSERT INTO workshops (name, image_path) VALUES (?, ?)";

    // 4. Execute the query
    try (Connection con = DBConnection.getConnection();
         PreparedStatement pst = con.prepareStatement(sql)) {
        
        // Safely set the values for the '?' placeholders
        pst.setString(1, workshopName);
        pst.setString(2, imagePath);

        int affectedRows = pst.executeUpdate();

        // 5. Give feedback and refresh the UI
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Workshop saved successfully!");
            
            // Clear the form for the next entry
            workshopNameField.setText("");
            imagePathField.setText("");
            
            // Refresh the table to show the new workshop
            loadWorkshops(); // Assumes you have a method named loadWorkshops()
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to save workshop: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
// 1. Get the selected row from the table
    int selectedRow = workshopsTable.getSelectedRow();

    // 2. Check if a row was actually selected
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a workshop from the table to delete.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return; // Stop the method if nothing is selected
    }

    // 3. Get the ID of the workshop from the first column (index 0) of the selected row
    int workshopId = (int) workshopsTable.getValueAt(selectedRow, 0);

    // 4. Ask the user for confirmation before deleting
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to delete this workshop?", 
        "Confirm Deletion", 
        JOptionPane.YES_NO_OPTION);

    // 5. Only proceed if the user clicked "Yes"
    if (confirm == JOptionPane.YES_OPTION) {
        String sql = "DELETE FROM workshops WHERE id = ?";

        try (Connection con = DBConnection.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setInt(1, workshopId); // Set the ID for the delete query

            int affectedRows = pst.executeUpdate();

            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(this, "Workshop deleted successfully!");
                loadWorkshops(); // Refresh the table to show the change
            } else {
                JOptionPane.showMessageDialog(this, "Deletion failed. Workshop not found.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error during deletion: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_deleteButtonActionPerformed

