    private void filterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterButtonActionPerformed
String selectedYear = (String) yearFilterComboBox.getSelectedItem();
    String selectedEvent = (String) eventFilterComboBox.getSelectedItem();
    String selectedWorkshop = (String) workshopFilterComboBox.getSelectedItem();

    java.util.List<Object> params = new java.util.ArrayList<>();
    
    // The base query that combines both of your registration tables
    String baseQuery = 
        "(SELECT id, student_name, study_year, branch, college_name, event_name, 'Event' as type, registration_date FROM registrations) " +
        "UNION ALL " +
        "(SELECT id, student_name, study_year, branch, college_name, event_name, 'Workshop' as type, registration_date FROM event_registrations)";

    StringBuilder whereClause = new StringBuilder(" WHERE 1=1");
    
    // Build the query safely using ? placeholders
    if (!"All Years".equals(selectedYear)) {
        whereClause.append(" AND study_year = ?");
        params.add(selectedYear);
    }
    
    // **CORRECTED LOGIC**: Prioritize the Event filter. If it's used, ignore the workshop filter.
    if (!"All Events".equals(selectedEvent)) {
        whereClause.append(" AND event_name = ? AND type = 'Event'");
        params.add(selectedEvent);
        workshopFilterComboBox.setSelectedIndex(0); // Reset the other dropdown for clarity
    } 
    // Only apply the workshop filter if the event filter is not being used.
    else if (!"All Workshops".equals(selectedWorkshop)) {
        whereClause.append(" AND event_name = ? AND type = 'Workshop'");
        params.add(selectedWorkshop);
    }
    
    String finalSql = "SELECT id, student_name, study_year, branch, college_name, event_name, registration_date FROM (" + baseQuery + ") AS combined_data" + whereClause.toString() + " ORDER BY id DESC";
    
    loadRegistrationData(finalSql, params);
    }//GEN-LAST:event_filterButtonActionPerformed

