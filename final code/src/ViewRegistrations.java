 private void loadRegistrationData(String sql, java.util.List<Object> params) {
       String[] columnNames = {"ID", "Student Name", "Year", "Branch", "College", "Event", "Date"};
        DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Cells cannot be edited by the user
            }
        };

        try (Connection con = DBConnection.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            // Safely set all the parameters in the list
            for (int i = 0; i < params.size(); i++) {
                pst.setObject(i + 1, params.get(i));
            }

            try (ResultSet rs = pst.executeQuery()) {
                while (rs.next()) {
                    Object[] row = {
                        rs.getInt("id"), rs.getString("student_name"), rs.getString("study_year"),
                        rs.getString("branch"), rs.getString("college_name"), rs.getString("event_name"),
                        rs.getString("registration_date")
                    };
                    tableModel.addRow(row);
                }
            }
            registrationsTable.setModel(tableModel);

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
