    private void loadWorkshops() {
    String[] columnNames = {"ID", "Workshop Name", "Image Path"};
    DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Make cells non-editable
        }
    };

    String sql = "SELECT id, name, image_path FROM workshops ORDER BY id DESC";

    try (Connection con = DBConnection.getConnection();
         PreparedStatement pst = con.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {

        while (rs.next()) {
            Object[] row = {
                rs.getInt("id"),
                rs.getString("name"),
                rs.getString("image_path")
            };
            tableModel.addRow(row);
        }
        // Assumes your JTable variable is named 'workshopsTable'
        workshopsTable.setModel(tableModel);

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to load workshops: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}

