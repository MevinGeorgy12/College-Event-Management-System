    private void loadWorkshopsIntoComboBox() {
    // Create a model to hold the workshop names for the combo box
    DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
    
    // Add a default "Select Workshop" option (optional)
    model.addElement("-- Select Workshop --"); 

    String sql = "SELECT name FROM workshops ORDER BY name ASC"; // Get names from the workshops table

    try (Connection con = DBConnection.getConnection();
         PreparedStatement pst = con.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {

        // Loop through the results and add each workshop name to the model
        while (rs.next()) {
            model.addElement(rs.getString("name"));
        }
        
        // Set the completed model to your combo box
        jComboBox3.setModel(model);

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to load workshops list: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}

