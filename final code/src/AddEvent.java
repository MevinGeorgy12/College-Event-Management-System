    private void loadEvents() {
    String[] columnNames = {"ID", "Event Name", "Image Path"};
    DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Make cells non-editable
        }
    };

    String sql = "SELECT id, name, image_path FROM events ORDER BY id DESC";

    try (Connection con = DBConnection.getConnection();
         PreparedStatement pst = con.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {

        while (rs.next()) {
            Object[] row = {
                rs.getInt("id"),
                rs.getString("name"),
                rs.getString("image_path")
            };
            tableModel.addRow(row);
        }
        // Make sure your JTable's variable name is 'eventsTable'
        eventsTable.setModel(tableModel);

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to load events: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}
