    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
       String eventName = eventNameField.getText();
    String imagePath = imagePathField.getText();

    if (eventName.isEmpty() || imagePath.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Event Name and Image Path are required.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String sql = "INSERT INTO events (name, image_path) VALUES (?, ?)";

    try (Connection con = DBConnection.getConnection();
         PreparedStatement pst = con.prepareStatement(sql)) {
        
        pst.setString(1, eventName);
        pst.setString(2, imagePath);

        if (pst.executeUpdate() > 0) {
            JOptionPane.showMessageDialog(this, "Event saved successfully!");
            eventNameField.setText("");
            imagePathField.setText("");
            loadEvents(); // Refresh table
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Failed to save event: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButton1ActionPerformed
       int selectedRow = eventsTable.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an event to delete.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int eventId = (int) eventsTable.getValueAt(selectedRow, 0);
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this event?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        String sql = "DELETE FROM events WHERE id = ?";
        try (Connection con = DBConnection.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {
            pst.setInt(1, eventId);
            if (pst.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Event deleted successfully!");
                loadEvents(); // Refresh table
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting event: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_deleteButton1ActionPerformed

